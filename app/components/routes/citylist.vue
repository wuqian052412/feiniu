			return {
				alphabet:['A','B','C','F','G','H','J','L','N','Q','S','T','X','Y','Z'],
				num_id:0,
			}
		},
		methods:{
			jumpTo(ele,index){
				document.body.scrollTop = document.getElementById(ele).offsetTop;
				this.num_id = index;
				
			}
		},
		computed:{
			getCurrentCity(){
				return this.$store.state.location_city;
			}
		},
		mounted(){
			var self = this;
			var cityBox = document.getElementsByClassName('citybox')[0];
			var city_li = cityBox.children[0].children;
			cityBox.onclick = function(e){
				e = e || window.event;
				var target = e.target || e.srcElement;
				//遍历对象，取消所有高亮
				for(var i = 0;i<city_li.length;i++){
					city_li[i].children[0].style.color = '#333';
				}
				if(target.className === 'J_city'){
					e.target.style.color = 'red';
					self.$store.dispatch('getCity',e.target.innerText);
					location.hash = '#/index/home'
				}
			}
			for(var i = 0;i<city_li.length;i++){
				if(city_li[i].children[0].innerText == this.getCurrentCity){
					city_li[i].children[0].style.color = '#C70034';
					city_li[i].children[0].style.background = '#FFFDEF';
				}
			}
		}
	}
